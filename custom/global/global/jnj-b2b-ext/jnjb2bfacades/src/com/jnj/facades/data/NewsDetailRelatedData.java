package com.jnj.facades.data;

/**
 * @author balinder.singh
 * 
 */
public class NewsDetailRelatedData
{
	private String headLine = null;
	private String summaryDesc = null;
	private String url = null;
	private String title = null;

	/**
	 * @return the headLine
	 */
	public String getHeadLine()
	{
		return headLine;
	}

	/**
	 * @param headLine
	 *           the headLine to set
	 */
	public void setHeadLine(final String headLine)
	{
		this.headLine = headLine;
	}

	/**
	 * @return the summaryDesc
	 */
	public String getSummaryDesc()
	{
		return summaryDesc;
	}

	/**
	 * @param summaryDesc
	 *           the summaryDesc to set
	 */
	public void setSummaryDesc(final String summaryDesc)
	{
		this.summaryDesc = summaryDesc;
	}

	/**
	 * @return the url
	 */
	public String getUrl()
	{
		return url;
	}

	/**
	 * @param url
	 *           the url to set
	 */
	public void setUrl(final String url)
	{
		this.url = url;
	}

	/**
	 * @return the title
	 */
	public String getTitle()
	{
		return title;
	}

	/**
	 * @param title
	 *           the title to set
	 */
	public void setTitle(final String title)
	{
		this.title = title;
	}



}
