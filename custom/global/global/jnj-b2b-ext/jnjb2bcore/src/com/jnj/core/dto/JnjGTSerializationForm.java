package com.jnj.core.dto;

public class JnjGTSerializationForm {
	
	private String gtin;
	private String serialNumber;
	private String batchNumber;
	private String expiryYear;
	private String expiryMonth;
	private String expiryDay;
	private String downloadType;
	/**
	 * @return the gtin
	 */
	public String getGtin() {
		return gtin;
	}
	/**
	 * @param gtin the gtin to set
	 */
	public void setGtin(String gtin) {
		this.gtin = gtin;
	}
	/**
	 * @return the serialNumber
	 */
	public String getSerialNumber() {
		return serialNumber;
	}
	/**
	 * @param serialNumber the serialNumber to set
	 */
	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}
	/**
	 * @return the batchNumber
	 */
	public String getBatchNumber() {
		return batchNumber;
	}
	/**
	 * @param batchNumber the batchNumber to set
	 */
	public void setBatchNumber(String batchNumber) {
		this.batchNumber = batchNumber;
	}
	/**
	 * @return the expiryYear
	 */
	public String getExpiryYear() {
		return expiryYear;
	}
	/**
	 * @param expiryYear the expiryYear to set
	 */
	public void setExpiryYear(String expiryYear) {
		this.expiryYear = expiryYear;
	}
	/**
	 * @return the expiryMonth
	 */
	public String getExpiryMonth() {
		return expiryMonth;
	}
	/**
	 * @param expiryMonth the expiryMonth to set
	 */
	public void setExpiryMonth(String expiryMonth) {
		this.expiryMonth = expiryMonth;
	}
	/**
	 * @return the expiryDay
	 */
	public String getExpiryDay() {
		return expiryDay;
	}
	/**
	 * @param expiryDay the expiryDay to set
	 */
	public void setExpiryDay(String expiryDay) {
		this.expiryDay = expiryDay;
	}
	/**
	 * @return the downloadType
	 */
	public String getDownloadType() {
		return downloadType;
	}
	/**
	 * @param downloadType the downloadType to set
	 */
	public void setDownloadType(String downloadType) {
		this.downloadType = downloadType;
	}
	
	
	
	
}
