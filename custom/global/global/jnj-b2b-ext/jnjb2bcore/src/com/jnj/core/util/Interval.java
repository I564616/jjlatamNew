package com.jnj.core.util;

public class Interval<T> {

    private final T start;
    private final T end;

    public Interval(final T start, final T end) {
        this.start = start;
        this.end = end;
    }

    public T getStart() {
        return start;
    }

    public T getEnd() {
        return end;
    }

}