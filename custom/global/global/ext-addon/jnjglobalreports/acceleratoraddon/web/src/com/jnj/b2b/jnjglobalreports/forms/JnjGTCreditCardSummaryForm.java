package com.jnj.b2b.jnjglobalreports.forms;

public class JnjGTCreditCardSummaryForm {

	private String payerId;
	private String OpenOrderValue;
	private String OpenDeliveryValue;
	private String AmountDue;
	private String CreditUsed;
	private String CreditLimit;
	private String OverUnderValue;
	private String CreditLimitUsed;
	
	public String getPayerId() {
		return payerId;
	}
	public void setPayerId(String payerId) {
		this.payerId = payerId;
	}
	public String getOpenOrderValue() {
		return OpenOrderValue;
	}
	public void setOpenOrderValue(String openOrderValue) {
		OpenOrderValue = openOrderValue;
	}
	public String getOpenDeliveryValue() {
		return OpenDeliveryValue;
	}
	public void setOpenDeliveryValue(String openDeliveryValue) {
		OpenDeliveryValue = openDeliveryValue;
	}
	public String getAmountDue() {
		return AmountDue;
	}
	public void setAmountDue(String amountDue) {
		AmountDue = amountDue;
	}
	public String getCreditUsed() {
		return CreditUsed;
	}
	public void setCreditUsed(String creditUsed) {
		CreditUsed = creditUsed;
	}
	public String getCreditLimit() {
		return CreditLimit;
	}
	public void setCreditLimit(String creditLimit) {
		CreditLimit = creditLimit;
	}
	public String getOverUnderValue() {
		return OverUnderValue;
	}
	public void setOverUnderValue(String overUnderValue) {
		OverUnderValue = overUnderValue;
	}
	public String getCreditLimitUsed() {
		return CreditLimitUsed;
	}
	public void setCreditLimitUsed(String creditLimitUsed) {
		CreditLimitUsed = creditLimitUsed;
	}
}
